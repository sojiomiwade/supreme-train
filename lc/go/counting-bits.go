// 0   0000     0
// 1   0001     1
// 2   0010     1
// 3   0011     2
// 4   0100     1 
// 5   0101  -- 2
// 6   0110  -- 2
// 7   0111  -- 3
// 8   1000  -- 1 <-- 1
// 9   1001  -- 2 <-- 1 + 1
// 10  1010  -- 2 <-- 2
// 11  1011  -- 3 <-- 2 + 1
// 12  1100  -- 2 <-- if x/2 is floor(x/2) same as bits(x/2) otherwise add 1
// 13  
// 14
// 15
// 16
//
func countBits(n int) []int {
    a := make([]int, 1 + n)
    for i:=1; i<=n; i++ {
        a[i] = a[i/2] + i%2
    }
    return a
}

